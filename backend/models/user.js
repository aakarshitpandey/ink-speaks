import * as mongoose from 'mongoose'
import { Schema } from "mongoose"

//creating user schema
const UserSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    age: {
        type: Number,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
})
const User = mongoose.model("User", UserSchema)

export default User